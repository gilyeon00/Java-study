package day01;

public class OpTest {

	public static void main(String[] args) {
		int a = 100;
		int b = 3;
		
		System.out.println("a+b " + a+b);	// 1003
		System.out.println("a+b " + (a+b));	// 103
		System.out.println("a+b " + String.valueOf(a+b));	// 103
		
		System.out.println("a*b " + a*b);	// 300
		

	}

}
